import { getExerciseQuestion } from './getExerciseQuestion';

describe('getExerciseQuestion', () => {
  it('должна возвращать корректный вопрос для простого текста', () => {
    expect(getExerciseQuestion('Наклон вперед')).toBe(
      'Сколько раз вы сделали упражнение "Наклон вперед"?'
    );
  });

  it('должна убирать скобки с содержимым', () => {
    expect(getExerciseQuestion('Наклон вперед (10 повторений)')).toBe(
      'Сколько раз вы сделали упражнение "Наклон вперед"?'
    );
  });

  it('должна правильно обрабатывать текст с несколькими скобками', () => {
    expect(getExerciseQuestion('Поднятие ног (лежа) (5 раз)')).toBe(
      'Сколько раз вы сделали упражнение "Поднятие ног"?'
    );
  });

  it('должна удалять лишние пробелы в начале и в конце', () => {
    expect(getExerciseQuestion('  Приседания  ')).toBe(
      'Сколько раз вы сделали упражнение "Приседания"?'
    );
  });

  it('должна корректно работать с пустой строкой', () => {
    expect(getExerciseQuestion('')).toBe(
      'Сколько раз вы сделали упражнение ""?'
    );
  });
});
